namespace NClap.Help
{
    /// <summary>
    /// Mode for including short name info in help.
    /// </summary>
    public enum ArgumentShortNameHelpMode
    {
        /// <summary>
        /// Do not include short names.
        /// </summary>
        Omit,

        /// <summary>
        /// Append short names to the end of the description.
        /// </summary>
        AppendToDescription
    }
}
