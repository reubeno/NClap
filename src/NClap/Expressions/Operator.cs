namespace NClap.Expressions
{
    /// <summary>
    /// Operator type.
    /// </summary>
    internal enum Operator
    {
        /// <summary>
        /// Sentinel invalid value.
        /// </summary>
        Unspecified = 0,

        /// <summary>
        /// Converts to lower-case.
        /// </summary>
        ConvertToLowerCase = 1,

        /// <summary>
        /// Converts to lower-case.
        /// </summary>
        ConvertToUpperCase = 2,
    }
}
